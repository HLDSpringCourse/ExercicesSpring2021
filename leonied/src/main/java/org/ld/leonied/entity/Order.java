package org.ld.leonied.entity;

public class Order {
    private int id;
    private String name;

    public Order() {}

    public Order(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
